# an operator is a symbol that performs a certain operation between operands
# such as :
# Arithmetic Operator : (+,-,*,/,%,**)
# example :
a = 5
b = 10
multi = a*b
print(multi)

x = 10
y = 20
print(x+y)
print(x-y)
print(x/y)
print(x % y)  # Remainder
print(x**y)  # x^y (double star indicate power)
# Relational Operator : (==,!=,>,<,>=,<=)
# example :
a = 20
b = 10
print(a == b)  # False
print(a != b)  # True
print(a >= b)
print(a <= b)
print(a > b)
print(a < b)
# Assignment Operator : (=,+=,-=,*=,/=,%=,**=)
# example :
a = 20
a = a+10
print(a)

# or
x = 10
x += 10
print(x)
# or
m = 5
m *= 10
print(m)
